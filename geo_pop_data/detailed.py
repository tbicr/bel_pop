def parse_nums(name, numbers):
    nums = numbers.split()
    if len(nums) == 9:
        result = [int(0 if n == '-' else n) for n in nums]
    elif len(nums) == 18:
        result = [
            int(nums[0] + nums[1]), int(nums[2] + nums[3]), int(nums[4] + nums[5]),
            int(nums[6] + nums[7]), int(nums[8] + nums[9]), int(nums[10] + nums[11]),
            int(nums[12] + nums[13]), int(nums[14] + nums[15]), int(nums[16] + nums[17]),
        ]
    elif nums[-3:] == ['-'] * 3 and len(nums[:-3]) == 12:
        result = [
            int(nums[0] + nums[1]), int(nums[2] + nums[3]), int(nums[4] + nums[5]),
            int(nums[6] + nums[7]), int(nums[8] + nums[9]), int(nums[10] + nums[11]),
            0, 0, 0
        ]
    elif len([n for n in nums if len(n) == 3 or n == '-']) == 9:
        cur = ''
        result = []
        for i, n in reversed(list(enumerate(nums))):
            if n == '-' and cur:
                result.append(0 if cur == '-' else int(cur))
                cur = '-'
            elif len(n) == 3 and cur:
                result.append(0 if cur == '-' else int(cur))
                cur = n
            else:
                cur = n + cur
        result.append(int(cur))
        result = list(reversed(result))
    else:
        raise ValueError(name + ' ' + numbers)

    assert len(result) == 9, name + ' ' + numbers
    assert result[0] == result[1] + result[2], name + ' ' + numbers + ' ' + str(result)
    assert result[0] == result[3] + result[6], name + ' ' + numbers + ' ' + str(result)
    assert result[3] == result[4] + result[5], name + ' ' + numbers + ' ' + str(result)
    assert result[6] == result[7] + result[8], name + ' ' + numbers + ' ' + str(result)

    return result


